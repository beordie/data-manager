package com.tipdm.framework.dmserver.core.scheduling.model;

import java.io.Serializable;

/**
 * Created by TipDM on 2017/1/10.
 * E-mail:devp@tipdm.com
 * 连接
 */
public class Link implements Serializable {

    private String id;//

    private String source;//源节点

    private String target;//目标节点

    private String inputPortId;//连接线上目标节点输入项的ID

    private String outputPortId;//源节点的输出项的ID

    private String d;

    private String runStyle;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getTarget() {
        return target;
    }

    public void setTarget(String target) {
        this.target = target;
    }

    public String getInputPortId() {
        return inputPortId;
    }

    public void setInputPortId(String inputPortId) {
        this.inputPortId = inputPortId;
    }

    public String getOutputPortId() {
        return outputPortId;
    }

    public void setOutputPortId(String outputPortId) {
        this.outputPortId = outputPortId;
    }

    public String getD() {
        return d;
    }

    public void setD(String d) {
        this.d = d;
    }

    public String getRunStyle() {
        return runStyle;
    }

    public void setRunStyle(String runStyle) {
        this.runStyle = runStyle;
    }
}
