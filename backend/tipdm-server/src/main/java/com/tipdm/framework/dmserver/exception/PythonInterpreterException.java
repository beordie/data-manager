public class PythonInterpreterException extends Exception{

    public PythonInterpreterException() {
        super();
    }

    public PythonInterpreterException(String msg) {
        super(msg);
    }

    public PythonInterpreterException(String msg, Throwable cause) {
        super(msg, cause);
    }

    public PythonInterpreterException(Throwable cause) {
        super(cause);
    }
}
